package com.tutorial;

public class Main {

    public static void main(String[] args) {
	    //Operator Assingment

        //Assignmeng Operator Jumlah
        int a = 1;

        a += 10; // a = a + 10
        System.out.println("nilai a =" + a);

        //Assignmeng Operator Pengurangan
        int b = 1;
        b -= 10; // b = b-10
        System.out.println("nilai b =" + b);

        //Assignmeng Operator Perkalian
        int c = 1;
        c *= 10; // c = c*10
        System.out.println("nilai c =" + c);

        //Assignmeng Operator Pembagian
        int d = 1;
        d /= 10; // d = d/10
        System.out.println("nilai d =" + d);

        //Assignmeng Operator Modulus
        int e = 1;
        e %= 10; // b = b%%10
        System.out.println("nilai e =" + e);


    }
}
